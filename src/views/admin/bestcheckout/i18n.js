function replaceTextNode(node, locale) {
  const value = node.nodeValue;
  const match = /^(\s*)(.*?)(\s*)$/s.exec(value);
  if (!match || !match[2]) return;
  const next = translate(match[2], locale);
  if (next !== match[2]) node.nodeValue = match[1] + next + match[3];
}

export function applyLocale(root, locale) {
  if (typeof document !== 'undefined' && document.documentElement) document.documentElement.lang = locale === 'zh' ? 'zh-CN' : 'en';
  if (locale !== 'zh' || !root || typeof document === 'undefined' || typeof NodeFilter === 'undefined') return;
  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT);
  const textNodes = [];
  let node = walker.nextNode();
  while (node) {
    if (!node.parentElement || !node.parentElement.closest('[data-i18n-skip]')) textNodes.push(node);
    node = walker.nextNode();
  }
  textNodes.forEach(function (textNode) { replaceTextNode(textNode, locale); });
  root.querySelectorAll('[aria-label], [placeholder], [title], [data-label]').forEach(function (element) {
    if (element.closest('[data-i18n-skip]')) return;
    ['aria-label', 'placeholder', 'title', 'data-label'].forEach(function (attribute) {
      const value = element.getAttribute(attribute);
      if (!value) return;
      const localized = ZH_ATTRIBUTES[value] || translate(value, locale);
      if (localized !== value) element.setAttribute(attribute, localized);
    });
  });
  if (document.documentElement) document.documentElement.lang = 'zh-CN';
}

export function renderLanguageSwitcher(locale, isOpen) {
  const isChinese = locale === 'zh';
  const currentFlag = '<span class="language-flag language-flag-' + (isChinese ? 'cn' : 'us') + '" aria-hidden="true"></span>';
  const currentLabel = isChinese ? '中文' : 'EN';
  const currentTitle = isChinese ? '语言' : 'Language';
  const menu = isOpen
    ? '<div class="language-menu" role="menu" aria-label="' + currentTitle + '"><button type="button" class="language-option ' + (!isChinese ? 'language-option-active' : '') + '" data-locale="en" role="menuitemradio" aria-checked="' + (!isChinese) + '"><span class="language-option-flag"><span class="language-flag language-flag-us" aria-hidden="true"></span></span><strong>English</strong>' + (!isChinese ? '<b>✓</b>' : '') + '</button><button type="button" class="language-option ' + (isChinese ? 'language-option-active' : '') + '" data-locale="zh" role="menuitemradio" aria-checked="' + isChinese + '"><span class="language-option-flag"><span class="language-flag language-flag-cn" aria-hidden="true"></span></span><strong>简体中文</strong>' + (isChinese ? '<b>✓</b>' : '') + '</button></div>'
    : '';
  return '<div class="language-widget" data-i18n-skip><button type="button" class="language-trigger" data-action="toggle-language-menu" aria-label="' + currentTitle + ' · ' + (isChinese ? '简体中文' : 'English') + '" aria-haspopup="menu" aria-expanded="' + (isOpen ? 'true' : 'false') + '"><span class="language-current-mark">' + currentFlag + '</span><strong>' + currentLabel + '</strong><i>' + (isOpen ? '⌃' : '⌄') + '</i></button>' + menu + '</div>';
}
